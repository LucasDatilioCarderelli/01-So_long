#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	size_t	len;

	len = ft_strlen(s) + 1;
	dup = malloc(sizeof(char) * len);
	if (dup == 0)
		return (0);
	ft_memcpy(dup, s, len);
	return (dup);
}
